    // Validation form
    private boolean validateFormNapTien() {
        // Kiểm tra mã khách hàng
        if (txtMaKH.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có thông tin khách hàng!");
            return false;
        }
        
        // Kiểm tra số tiền nạp
        if (txtSoTienNap.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số tiền nạp!");
            txtSoTienNap.requestFocus();
            return false;
        }
        
        try {
            double soTien = Double.parseDouble(txtSoTienNap.getText().replace(",", ""));
            if (soTien <= 0) {
                JOptionPane.showMessageDialog(this, "Số tiền nạp phải lớn hơn 0!");
                txtSoTienNap.requestFocus();
                return false;
            }
            if (soTien > 50000000) { // Giới hạn 50 triệu
                JOptionPane.showMessageDialog(this, "Số tiền nạp không được vượt quá 50,000,000 VNĐ!");
                txtSoTienNap.requestFocus();
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số tiền nạp không hợp lệ!");
            txtSoTienNap.requestFocus();
            return false;
        }
        
        return true;
    }
    
