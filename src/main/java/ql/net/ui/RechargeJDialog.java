    public void loadLichSuGiaoDichToTable() {
        try {
            String sql = "SELECT MaGiaoDich, MaKH, LoaiGiaoDich, SoTien, NgayGiaoDich FROM LichSuGiaoDich ORDER BY NgayGiaoDich DESC";
            java.sql.ResultSet rs = ql.net.util.XJdbc.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) tblLichSuGiaoDich.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("MaGiaoDich"),
                    rs.getString("MaKH"),
                    rs.getString("LoaiGiaoDich"),
                    rs.getBigDecimal("SoTien"),
                    rs.getTimestamp("NgayGiaoDich")
                });
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải lịch sử giao dịch: " + e.getMessage());
        }
    }
    
    // Generate mã giao dịch
    private String generateMaGiaoDich() {
        try {
            String sql = "SELECT COUNT(*) as total FROM LichSuGiaoDich";
            String count = ql.net.util.XJdbc.getValue(sql);
            int nextNumber = Integer.parseInt(count != null ? count : "0") + 1;
            return String.format("GD%04d", nextNumber);
        } catch (Exception e) {
            return "GD" + String.format("%04d", (int)(System.currentTimeMillis() % 10000));
        }
    }
    
